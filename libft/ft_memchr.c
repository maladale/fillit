#include <stddef.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t i;

	if (n + 1 == 0)
		return (NULL);
	i = 0;
	while (i < n)
	{
		if (((unsigned char*)s)[i] == (unsigned char)c)
			return ((void*)(s + i));
		i++;
	}
	return (NULL);
}
