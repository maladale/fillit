char	*ft_strcat(char *to, char const *from)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (to[i] != '\0')
		i++;
	while (from[j] != '\0')
	{
		to[i] = from[j];
		j++;
		i++;
	}
	to[i] = '\0';
	return (to);
}
